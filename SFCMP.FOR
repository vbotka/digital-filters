      SUBROUTINE SFCMP(NFP,FC,FILTR)
C
C     CALCULATE FILTER COEFFICIENTS
C
      INTEGER NFP
      DOUBLE PRECISION FILTR(2*NFP+1),FC,OMEGA,PI,F1,F2,F3,SMARG
      PARAMETER(PI=3.141592654)
C
      OMEGA=2.0*PI*FC
      F3=NFP+1
      DO 100 I=1,NFP
	    F1=I
	    F2=F1*OMEGA
	    SMARG=PI*F1/F3
	    FILTR(NFP+I+1)=SIN(F2)/(PI*F1)*SIN(SMARG)/SMARG
	    FILTR(NFP-I+1)=FILTR(NFP+I+1)
100   CONTINUE
      FILTR(NFP+1)=2.0*FC
C
      RETURN
      END
