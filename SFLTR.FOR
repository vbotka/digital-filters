      SUBROUTINE SFLTR(N,Y,FILTR,NFP,FC)
C
C     SMOOTH EQUIDISTANTLY SPACED FUNCTION VALUES
C
      INTEGER N,NFP
      DOUBLE PRECISION Y(N),FILTR(N),FC
C
C     FILTER COMPUTATION
      CALL SFCMP(NFP,FC,FILTR)
C
C     DATA SMOOTHING
      CALL SFAPL(N,Y,NFP,FILTR)
C
      RETURN
      END
